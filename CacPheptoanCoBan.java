public class CacPheptoanCoBan {
    public static void main(String[] args) {
        int a = 11;
        int b = 5;
        System.out.println("a = " +a);
        System.out.println("B = " +b);
        int kq = a+b;
        // Phép cộng
        System.out.println("a+b = " +(a+b));
        // Gán biến
        System.out.println("a+b = " +kq);

        System.out.println("a-b = " +(a-b));
        System.out.println("a*b = " +(a*b));
        System.out.println("a%b = " +(a%b));
        System.out.println("a/b = " +((double)a/b));
    }
}
